char* sRGB(int r, int g, int b)
{
	static char out[6];
	string output;
	stringstream oss;

  //if (r == 255 && g == 255 && b == 255)
  //  r = g = b = 0;

	if (r < 16 )
		oss << 0;
	oss << hex << r;
	
	if (g < 16 )
		oss << 0;
	oss << hex << g;
	
	if (b < 16 )
		oss << 0;
	oss << hex << b;
	
	output = oss.str();
	for (int i = 0; i < 6; i++)
		out[i] = output[i];

	return out;
}


char* aci_to_sRGB(int aci)
 {
 	aci = abs(aci);			// hidden layers have negative color values
 	if (aci<10 || aci>249)	// values of these ranges are special colors
 	{
 		switch (aci)
 		{
 			case 1: return sRGB(255,0,0);		// basic colors
 			case 2: return sRGB(255,255,0);
 			case 3: return sRGB(0,255,0);
 			case 4: return sRGB(0,255,255);
 			case 5: return sRGB(0,0,255);
 			case 6: return sRGB(255,0,255);
 			case 7: return sRGB(255,255,255);
 			case 8: return sRGB(128,128,128);
 			case 9: return sRGB(192,192,192);
 			case 250: return sRGB(51,51,51);		// grey shades
 			case 251: return sRGB(91,91,91);
 			case 252: return sRGB(132,132,132);
 			case 253: return sRGB(173,173,173);
 			case 254: return sRGB(214,214,214);
 			case 255: return sRGB(255,255,255);
 			case 256:							// "by layer"
 			// Here you should decide how to handle "by layer" logical color.
 			// Maybe it is a good idea to return a value like -1.
 			// The outer code will find what is the color of the layer which
 			// this entity belongs to.
 				return NULL;
 		}
 	}
 	// for all the rest of ACI codes
 	float H,S,L,	R,G,B;
 	int reminder = aci % 10;					
 	H = 1.5f * (aci - reminder - 10);	// hue in range 0-360
 	S = ((aci % 2) ? 0.5f : 1.0f);		// odd colors have 50% of saturation, even - 100%
 	// set lighteness, the last digit of aci code stands for this
 	if (reminder == 0 || reminder == 1) L = 1.0f; 
 	if (reminder == 2 || reminder == 3) L = 0.8f;
 	if (reminder == 4 || reminder == 5) L = 0.6f;
 	if (reminder == 6 || reminder == 7) L = 0.5f;
 	if (reminder == 8 || reminder == 9) L = 0.3f;
 	// here we have H,S,L set already
 	// let's convert it to RGB, first without consideration of S and L
 	if (H<=120)
 	{
 		R = (120-H)/60;
 		G = H/60;
 		B = 0;
 	}
  	if (H>120 && H<=240)
 	{
 		R = 0;
 		G = (240-H)/60;
 		B = (H-120)/60;
 	}
 	if (H>240 && H<=360)
 	{
 		R = (H-240)/60;
 		G = 0;
 		B = (360-H)/60;
 	}
 	R = min(R, 1);
 	G = min(G, 1);
 	B = min(B, 1);
 	// influence of S and L
 	float max_value = max(R,max(G,B));
 	R = (max_value-S*(max_value-R)) * L * 255;
 	G = (max_value-S*(max_value-G)) * L * 255;
 	B = (max_value-S*(max_value-B)) * L * 255;
 	return sRGB(R,G,B);
 }


