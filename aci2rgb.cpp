char* acif_to_sRGB(int aci)
{
  aci = abs(aci);			// hidden layers have negative color values
  if (aci < 0 || aci>256)	// values of these ranges are special colors
    return NULL;
  if (aci == 0) return "byBlock"; else
  if (aci == 256) return "byLayer"; else
  return sRGB(dxfColors[aci][0] * 255, dxfColors[aci][1] * 255, dxfColors[aci][2] * 255);
}

